from PySide6.QtWidgets import QMessageBox, QDialog, QVBoxLayout, QHBoxLayout, QLabel, QPushButton, QFrame
from PySide6.QtCore import Qt

class FramelessMessageBox(QDialog):
    def __init__(self, icon, title, text, buttons=QMessageBox.Ok, parent=None):
        super().__init__(parent)
        self.setWindowFlags(Qt.FramelessWindowHint | Qt.Dialog)
        self.setAttribute(Qt.WA_TranslucentBackground)
        
        layout = QVBoxLayout(self)
        layout.setContentsMargins(10, 10, 10, 10)
        
        self.frame = QFrame()
        self.frame.setObjectName("card")
        
        theme = "dark"
        if parent and hasattr(parent, "file_manager"):
            theme = parent.file_manager.db.get_setting("theme", "dark")
            if theme == "system":
                import winreg
                try:
                    registry = winreg.ConnectRegistry(None, winreg.HKEY_CURRENT_USER)
                    key = winreg.OpenKey(registry, r"Software\Microsoft\Windows\CurrentVersion\Themes\Personalize")
                    value, _ = winreg.QueryValueEx(key, "AppsUseLightTheme")
                    theme = "light" if value == 1 else "dark"
                except Exception:
                    theme = "dark"
                    
        if theme == "light":
            bg_color = "#f3f4f6"
            border_color = "#e5e7eb"
            text_color = "#1f2937"
            btn_bg = "#ffffff"
            btn_border = "#e5e7eb"
            btn_text = "#1f2937"
            accent_bg = "qlineargradient(x1:0, y1:0, x2:1, y2:0, stop:0 #00c6ff, stop:1 #0072ff)"
            accent_text = "#ffffff"
        else:
            bg_color = "#121318"
            border_color = "#2a2d3a"
            text_color = "#f1f5f9"
            btn_bg = "#1c1e28"
            btn_border = "#3c4054"
            btn_text = "#f1f5f9"
            accent_bg = "qlineargradient(x1:0, y1:0, x2:1, y2:0, stop:0 #00c6ff, stop:1 #0072ff)"
            accent_text = "#ffffff"
            
        self.frame.setStyleSheet(f"""
            QFrame#card {{
                background-color: {bg_color};
                border: 2px solid {border_color};
                border-radius: 16px;
            }}
            QLabel {{
                color: {text_color};
                border: none;
                background: transparent;
            }}
            QPushButton {{
                background-color: {btn_bg};
                border: 1px solid {btn_border};
                border-radius: 8px;
                padding: 8px 18px;
                color: {btn_text};
                font-weight: 500;
                min-width: 80px;
            }}
            QPushButton:hover {{
                background-color: {btn_bg}dd;
            }}
            QPushButton#accent_btn {{
                background: {accent_bg};
                border: none;
                color: {accent_text};
                font-weight: 600;
            }}
        """)
        
        frame_layout = QVBoxLayout(self.frame)
        frame_layout.setContentsMargins(20, 20, 20, 20)
        frame_layout.setSpacing(16)
        
        content_layout = QHBoxLayout()
        content_layout.setSpacing(16)
        
        icon_label = QLabel()
        icon_label.setFixedSize(48, 48)
        
        icon_char = "ℹ️"
        icon_color = "#007acc"
        if icon == QMessageBox.Warning:
            icon_char = "⚠️"
            icon_color = "#ff9500"
        elif icon == QMessageBox.Critical:
            icon_char = "❌"
            icon_color = "#ff3b30"
        elif icon == QMessageBox.Question:
            icon_char = "❓"
            icon_color = "#5856d6"
            
        icon_label.setText(icon_char)
        icon_label.setAlignment(Qt.AlignCenter)
        icon_label.setStyleSheet(f"""
            font-size: 24px;
            background-color: {icon_color}22;
            border-radius: 24px;
            border: 2px solid {icon_color};
        """)
        content_layout.addWidget(icon_label)
        
        text_label = QLabel(text)
        text_label.setWordWrap(True)
        text_label.setStyleSheet("font-size: 14px; font-weight: 500;")
        content_layout.addWidget(text_label, 1)
        
        frame_layout.addLayout(content_layout)
        
        btn_layout = QHBoxLayout()
        btn_layout.addStretch()
        
        if buttons & QMessageBox.Ok:
            btn_ok = QPushButton("OK")
            btn_ok.setObjectName("accent_btn")
            btn_ok.clicked.connect(lambda: self.done(QMessageBox.Ok))
            btn_layout.addWidget(btn_ok)
            
        if buttons & QMessageBox.Yes:
            btn_yes = QPushButton("Yes")
            btn_yes.setObjectName("accent_btn")
            btn_yes.clicked.connect(lambda: self.done(QMessageBox.Yes))
            btn_layout.addWidget(btn_yes)
            
        if buttons & QMessageBox.No:
            btn_no = QPushButton("No")
            btn_no.clicked.connect(lambda: self.done(QMessageBox.No))
            btn_layout.addWidget(btn_no)
            
        if buttons & QMessageBox.Cancel:
            btn_cancel = QPushButton("Cancel")
            btn_cancel.clicked.connect(lambda: self.done(QMessageBox.Cancel))
            btn_layout.addWidget(btn_cancel)
            
        frame_layout.addLayout(btn_layout)
        layout.addWidget(self.frame)

    @staticmethod
    def show_info(parent, title, text, buttons=QMessageBox.Ok):
        msg = FramelessMessageBox(QMessageBox.Information, title, text, buttons, parent)
        msg.open()
        return msg

    @staticmethod
    def show_warning(parent, title, text, buttons=QMessageBox.Ok):
        msg = FramelessMessageBox(QMessageBox.Warning, title, text, buttons, parent)
        msg.open()
        return msg

    @staticmethod
    def show_critical(parent, title, text, buttons=QMessageBox.Ok):
        msg = FramelessMessageBox(QMessageBox.Critical, title, text, buttons, parent)
        msg.open()
        return msg
